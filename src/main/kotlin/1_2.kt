fun main(){
    var num1:Int=10
    val num2:Double=num1.toDouble()
    println("Integer value to double:$num2")

    var a:String = "10"
    val b:String=a.toString()
    println("String value to integer:$b")

    var c:String="11.12"
    val d:Double=c.toDouble()
    println("String value to double:$d")

}